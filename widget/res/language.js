!function(window){
	var l = {
		change: function(){
			var language = $api.getStorage(CONFIG.KEY.LANGUAGE) || 1,
					_doms = $api.domAll('[lan]');

			language = language == 1 ? 'english' : 'french';

			[].forEach.call(_doms, function(dom, index){
				var _key = $api.attr(dom, 'lan');
				if(LANGUAGE[language][_key]){
					switch(dom.tagName){
						case 'INPUT':
						case 'TEXTAREA':
							$api.attr(dom, 'placeholder', LANGUAGE[language][_key]);
							break;
						default:
							$api.text(dom, LANGUAGE[language][_key]);
							break;
					}
				}
			});
		},
		init: function(args){
			var self = this;

			this.change();

			api.addEventListener({
				name: 'languageChange'
			}, function(ret){
				self.change();
			});
		}
	};

	window.L = l;
}(window);
